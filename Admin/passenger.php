        $(document).ready(function() {
            // Initialize modals and load data
            $('#insertModal').click(function() {
                $('#passengerModal').modal('show');
                $('#passengerForm')[0].reset();
            });
            
            // Initial data loading
            displayData();
            
            // Create user record
            $('#passengerForm').submit(function(e) {
                e.preventDefault();
                
                $.ajax({
                    type: 'POST',
                    url: 'passengerOperation.php?action=create_passenger',
                    data: $(this).serialize(),
                    dataType: "json",
                    success: function(res) {
                        if (res.status === 'success') {
                            showSuccess(res.message, function() {
                                $('#passengerModal').modal('hide');
                                displayData();
                            });
                        } else {
                            showError(res.message);
                        }
                    },
                    error: function(xhr, status, error) {
                        showError('An error occurred: ' + error);
                    }
                });
            });
            
            // Edit user record
            $(document).on('click', '.editBtn', function() {
                const passengerData = {
                    id: $(this).data('id'),
                    first_name: $(this).data('first_name'),
                    last_name: $(this).data('last_name'),
                    email: $(this).data('email'),
                    phone: $(this).data('phone'),
                    gender: $(this).data('gender'),
                    passport_no: $(this).data('passport_no'),
                    date_of_birth: $(this).data('date_of_birth'),
                    nationality: $(this).data('nationality'),
                    address: $(this).data('address')
                };
                
                $('#edit_id').val(passengerData.id);
                $('#edit_first_name').val(passengerData.first_name);
                $('#edit_last_name').val(passengerData.last_name);
                $('#edit_email').val(passengerData.email);
                $('#edit_phone').val(passengerData.phone);
                $('#edit_gender').val(passengerData.gender);
                $('#edit_passport_no').val(passengerData.passport_no);
                $('#edit_DOB').val(passengerData.date_of_birth);
                $('#edit_nationality').val(passengerData.nationality);
                $('#edit_address').val(passengerData.address);
                
                $('#edit_passengerModal').modal('show');
            });
            
            // Update user record
            $('#edit_passengerForm').submit(function(e) {
                e.preventDefault();
                const submitBtn = $(this).find('[type="submit"]');
                submitBtn.prop('disabled', true).html('<i class="fas fa-spinner fa-spin"></i> Updating...');
                const formData = {
                  edit_id: $('#edit_id').val(),
                  edit_first_name: $('#edit_first_name').val(),
                  edit_last_name: $('#edit_last_name').val(),
                  edit_email: $('#edit_email').val(),
                  edit_phone: $('#edit_phone').val(),
                  edit_gender: $('#edit_gender').val(),
                  edit_passport_no: $('#edit_passport_no').val(),
                  edit_DOB: $('#edit_DOB').val(),
                  edit_nationality: $('#edit_nationality').val(),
                  edit_address: $('#edit_address').val()
                };
                $.ajax({
                    url: 'passengerOperation.php?action=update_passenger',
                    method: 'POST',
                    data: $(this).serialize(),
                    dataType: 'json',
                    success: function(response) {
                        if(response.status === 'success') {
                            showSuccess(response.message, function() {
                                $('#edit_passengerModal').modal('hide');
                                displayData();
                            });
                        } else {
                            showError(response.message);
                        }
                    },
                    error: function(xhr) {
                        showError('An error occurred: ' + xhr.statusText);
                    },
                    complete: function() {
                        submitBtn.prop('disabled', false).html('Update passenger');
                    }
                });
            });
            // Delete user record
            $(document).on('click', '.deleteBtn', function() {
                const passenger_id = $(this).data('id');
                
                Swal.fire({
                    title: 'Are you sure?',
                    text: "You won't be able to revert this!",
                    icon: 'warning',
                    showCancelButton: true,
                    confirmButtonColor: '#3085d6',
                    cancelButtonColor: '#d33',
                    confirmButtonText: 'Yes, delete it!'
                }).then((result) => {
                    if (result.isConfirmed) {
                        $.ajax({
                            type: 'POST',
                            url: 'passengerOperation.php?action=delete_passenger',
                            data: { id: passenger_id },
                            dataType: 'json',
                            success: function(res) {
                                if (res.status === 'success') {
                                    showSuccess(res.message, function() {
                                        displayData();
                                    });
                                } else {
                                    showError(res.message);
                                }
                            },
                            error: function(xhr, status, error) {
                                showError('An error occurred: ' + error);
                            }
                        });
                    }
                });
            });
            
            // Display user data in table
            function displayData() {
                $.ajax({
                    url: 'passengerOperation.php?action=display_passenger',
                    dataType: 'json',
                    success: function(response) {
                        // Check if response is valid and contains data
                        if (!response || !Array.isArray(response)) {
                            showError('Invalid data received from server');
                            return;
                        }
                        
                        let tableData = '';
                        response.forEach(row => {
                            tableData += `
                            <tr>
                                <td>${row.passenger_id || ''}</td>
                                <td>${row.first_name || ''}</td>
                                <td>${row.last_name || ''}</td>
                                <td>${row.email || ''}</td>
                                <td>${row.phone || ''}</td>
                                <td>${row.gender || ''}</td>
                                <td>${row.passport_no || ''}</td>
                                <td>${row.date_of_birth || ''}</td>
                                <td>${row.nationality || ''}</td>
                                <td>${row.address || ''}</td>
                                <td>${row.created_at || ''}</td>
                                <td>
                                    <button class="btn btn-warning btn-sm editBtn" 
                                        data-id="${row.passenger_id}" 
                                        data-first_name="${row.first_name}"
                                        data-last_name="${row.last_name}"
                                        data-email="${row.email}"
                                        data-phone="${row.phone}"
                                        data-gender="${row.gender}"
                                        data-passport_no="${row.passport_no}"
                                        data-date_of_birth="${row.date_of_birth}"
                                        data-nationality="${row.nationality}"
                                        data-address="${row.address}">
                                        Edit
                                    </button>
                                    <button class="btn btn-danger btn-sm deleteBtn" 
                                        data-id="${row.passenger_id}">
                                        Delete
                                    </button>
                                </td>
                            </tr>`;
                        });
                        
                        // Check if DataTable exists before destroying
                        if ($.fn.DataTable && $.fn.DataTable.isDataTable('#dataTable')) {
                            $('#dataTable').DataTable().destroy();
                        }
                        
                        $('#dataTable tbody').html(tableData);
                        initDataTable();
                    },
                    error: function(xhr, status, error) {
                        showError('Failed to load user data: ' + error);
                    }
                });
            }
            
            // Initialize DataTable
            function initDataTable() {
                $('#dataTable').DataTable({
                    paging: true,
                    searching: true,
                    ordering: true,
                    responsive: true
                });
            }

            
